#include "ft_printf.h"

char	*flag_0_and_num_min(char *s, t_strf *f)
{
	int w;
	int l;

	w = f->width;
	l = ft_strlen(s);
	s[0] = '0';
	while (w - l > 0)
	{
		s = ft_strjoin("0", s);
		w--;
	}
	s[0] = '-';
	return (s);
}
